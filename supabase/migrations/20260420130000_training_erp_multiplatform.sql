CREATE OR REPLACE FUNCTION public.next_employee_no(p_now timestamptz DEFAULT now())
RETURNS text
LANGUAGE plpgsql
AS $$
DECLARE
  v_yy text := to_char(p_now, 'YY');
  v_prefix text := v_yy || '-';
  v_last text;
  v_seq integer := 1;
BEGIN
  SELECT employee_no
    INTO v_last
    FROM public.app_users
   WHERE employee_no LIKE v_prefix || '%'
   ORDER BY employee_no DESC
   LIMIT 1;
  IF v_last IS NOT NULL THEN
    v_seq := COALESCE(NULLIF(split_part(v_last, '-', 2), '')::integer, 0) + 1;
  END IF;
  RETURN v_prefix || lpad(v_seq::text, 4, '0');
END;
$$;

