CREATE OR REPLACE FUNCTION public.guard_warehouse_limit()
RETURNS trigger
LANGUAGE plpgsql
AS $$
DECLARE
  v_count integer;
BEGIN
  SELECT count(*) INTO v_count FROM public.warehouses;
  IF v_count >= 20 THEN
    RAISE EXCEPTION 'Maximum warehouses reached (20)';
  END IF;
  RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS trg_guard_warehouse_limit ON public.warehouses;
CREATE TRIGGER trg_guard_warehouse_limit
BEFORE INSERT ON public.warehouses
FOR EACH ROW
