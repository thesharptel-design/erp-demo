GRANT EXECUTE ON FUNCTION public.touch_temp_approval_attachments(text, uuid, integer) TO service_role;

CREATE OR REPLACE FUNCTION public.validate_approval_doc_attachment_insert()
RETURNS trigger
LANGUAGE plpgsql
AS $$
DECLARE
  v_related_writer uuid;
  v_related_status text;
BEGIN
  IF NEW.created_by IS NULL OR NEW.created_by <> auth.uid() THEN
    RAISE EXCEPTION '첨부 등록자는 현재 로그인 사용자와 같아야 합니다.';
  END IF;

  IF NEW.approval_doc_id IS NOT NULL THEN
    IF NOT EXISTS (
      SELECT 1
      FROM public.approval_docs d
      WHERE d.id = NEW.approval_doc_id
        AND (
          public.is_system_admin_user(auth.uid())
          OR d.writer_id = auth.uid()
        )
    ) THEN
      RAISE EXCEPTION '현재 문서에 첨부를 등록할 권한이 없습니다.';
    END IF;
    NEW.status := COALESCE(NULLIF(NEW.status, ''), 'linked');
    NEW.expires_at := NULL;
  ELSE
    IF COALESCE(trim(NEW.draft_session_key), '') = '' THEN
      RAISE EXCEPTION '임시 첨부에는 draft_session_key가 필요합니다.';
    END IF;
    NEW.status := COALESCE(NULLIF(NEW.status, ''), 'temp');
    IF NEW.last_autosave_at IS NULL THEN
      NEW.last_autosave_at := now();
    END IF;
    IF NEW.expires_at IS NULL THEN
      NEW.expires_at := now() + interval '72 hours';
    END IF;
  END IF;

  IF NEW.attachment_kind = 'approval_doc' THEN
    SELECT d.writer_id, d.status
      INTO v_related_writer, v_related_status
      FROM public.approval_docs d
     WHERE d.id = NEW.related_approval_doc_id;

    IF v_related_writer IS NULL THEN
      RAISE EXCEPTION '첨부할 결재문서를 찾을 수 없습니다.';
    END IF;
    IF v_related_writer <> NEW.created_by THEN
      RAISE EXCEPTION '본인이 기안한 문서만 첨부할 수 있습니다.';
    END IF;
    IF COALESCE(v_related_status, '') <> 'approved' THEN
      RAISE EXCEPTION '최종 결재 완료(approved) 문서만 첨부할 수 있습니다.';
    END IF;
  END IF;

  RETURN NEW;
END;
$$;

CREATE OR REPLACE FUNCTION public.link_temp_approval_attachments(
  p_draft_session_key text,
  p_approval_doc_id bigint,
  p_actor_id uuid
)
RETURNS integer
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_writer uuid;
  v_count integer := 0;
BEGIN
  IF auth.uid() IS NULL OR auth.uid() <> p_actor_id THEN
    RAISE EXCEPTION '권한이 없습니다.';
  END IF;
  IF COALESCE(trim(p_draft_session_key), '') = '' THEN
    RETURN 0;
  END IF;

  SELECT d.writer_id INTO v_writer
    FROM public.approval_docs d
   WHERE d.id = p_approval_doc_id;

  IF v_writer IS NULL THEN
    RAISE EXCEPTION '연결할 결재문서를 찾을 수 없습니다.';
  END IF;
  IF v_writer <> p_actor_id AND NOT public.is_system_admin_user(auth.uid()) THEN
    RAISE EXCEPTION '해당 문서에 첨부를 연결할 권한이 없습니다.';
  END IF;

  UPDATE public.approval_doc_attachments
     SET approval_doc_id = p_approval_doc_id,
         status = 'linked',
         expires_at = NULL
   WHERE draft_session_key = p_draft_session_key
     AND created_by = p_actor_id
     AND status = 'temp'
     AND approval_doc_id IS NULL;

  GET DIAGNOSTICS v_count = ROW_COUNT;
  RETURN v_count;
END;
$$;

REVOKE ALL ON FUNCTION public.link_temp_approval_attachments(text, bigint, uuid) FROM PUBLIC;
