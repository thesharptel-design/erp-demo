CREATE OR REPLACE FUNCTION public.touch_temp_approval_attachments(
  p_draft_session_key text,
  p_actor_id uuid,
  p_ttl_hours integer DEFAULT 72
)
RETURNS integer
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_count integer := 0;
BEGIN
  IF auth.uid() IS NULL OR auth.uid() <> p_actor_id THEN
    RAISE EXCEPTION '권한이 없습니다.';
  END IF;
  IF COALESCE(trim(p_draft_session_key), '') = '' THEN
    RETURN 0;
  END IF;

  UPDATE public.approval_doc_attachments
     SET last_autosave_at = now(),
         expires_at = now() + make_interval(hours => GREATEST(p_ttl_hours, 1))
   WHERE draft_session_key = p_draft_session_key
     AND created_by = p_actor_id
     AND status = 'temp'
     AND approval_doc_id IS NULL;

  GET DIAGNOSTICS v_count = ROW_COUNT;
  RETURN v_count;
END;
$$;

